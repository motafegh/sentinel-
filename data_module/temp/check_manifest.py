import json
m = json.loads(open("data/exports/sentinel-v3-smartbugs-2026-06-13/manifest.json").read())
print("n_contracts:", m.get("n_contracts"))
print("n_with_graphs:", m.get("n_with_graphs"))
print("schema_version:", m.get("schema_version"))
train_shas = m.get("splits", {}).get("train", [])
val_shas = m.get("splits", {}).get("val", [])
test_shas = m.get("splits", {}).get("test", [])
print("splits train:", len(train_shas))
print("splits val:", len(val_shas))
print("splits test:", len(test_shas))
tv = set(train_shas) & set(val_shas)
tt = set(train_shas) & set(test_shas)
vt = set(val_shas) & set(test_shas)
print("train∩val:", len(tv), "train∩test:", len(tt), "val∩test:", len(vt))
